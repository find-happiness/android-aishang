package com.aishang.app.data.model;

public class JResult {

	private String result;

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	@Override public String toString() {
		return "JResult{" +
				"result='" + result + '\'' +
				'}';
	}
}
